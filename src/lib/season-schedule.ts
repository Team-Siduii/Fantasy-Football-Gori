export function groupFixturesByRound(fixtures: SeasonFixture[]): Array<{ round: number; fixtures: SeasonFixture[] }> {
  const grouped = new Map<number, SeasonFixture[]>();

  for (const fixture of fixtures) {
    const list = grouped.get(fixture.round) ?? [];
    list.push(fixture);
    grouped.set(fixture.round, list);
  }

  return [...grouped.entries()]
    .sort((a, b) => a[0] - b[0])
    .map(([round, roundFixtures]) => ({
      round,
      fixtures: [...roundFixtures].sort((a, b) => a.kickoffAt.localeCompare(b.kickoffAt) || a.home.localeCompare(b.home)),
    }));
}

export function getCurrentOrNextRound(fixtures: SeasonFixture[], now: Date): number | null {
  if (fixtures.length === 0) {
    return null;
  }

  const sorted = [...fixtures].sort((a, b) => a.kickoffAt.localeCompare(b.kickoffAt));
  const upcoming = sorted.find((fixture) => new Date(fixture.kickoffAt).getTime() >= now.getTime());

  if (upcoming) {
    return upcoming.round;
  }

  return sorted[sorted.length - 1]?.round ?? null;
}
